#variable de tipo entero
numero = 147
print(numero)
print(type(numero))
#variable de tipo float
numero = 147.5
print(numero)
print(type(numero))
#variable de tipo complejo
complejo = 1 + 2j
print(complejo)
print(type(complejo))
#variable de tipo palabra
palabra = 'Hola'
print(palabra)
print(type(palabra))
#variable de tipo logico
bandera = True
print(bandera)
print(type(bandera))
#variable de tipo vacio
vacio = None
print(vacio)
print(type(vacio))

